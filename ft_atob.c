#include "minitalk.h"

int	ft_atob(int c)
{
	int	res;
	int	i;

	i = 1;
	res = 0;
	while (c > 0)
	{
		res += i * (c % 2);
		c /= 2;
		i *= 10;
	}
	return (res);
}
