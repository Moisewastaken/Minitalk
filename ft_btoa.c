#include "minitalk.h"

int	ft_btoa(int bits[])
{
	int	res;
	int	base;
	int	i;

	base = 1;
	res = 0;
	i = 8;
	while (--i >= 0)
	{
		res += bits[i] * base;
		base *= 2;
	}
	return (res);
}
